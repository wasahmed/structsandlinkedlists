#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		k;
	size_t	len;

	i = 0;
	k = 0;
	len = 0;
	while (s1[i] != '\0')
		i++;
	while (len < n && s2[k] != '\0')
	{
		s1[i + k] = s2[k];
		k++;
		len++;
	}
	s1[i + k] = '\0';
	return (s1);
}
